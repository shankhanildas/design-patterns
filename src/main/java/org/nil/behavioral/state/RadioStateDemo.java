package org.nil.behavioral.state;

public class RadioStateDemo {

	public static void main(String[] args) {
		Radio radio = new Radio();
		
		System.out.println(radio);
		
		radio.tune();
		System.out.println(radio);
		
		radio.tune();
		System.out.println(radio);
		
		radio.tune();
		System.out.println(radio);
		
		radio.tune();
		System.out.println(radio);
	}

}
